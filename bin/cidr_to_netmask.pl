#!/usr/bin/env perl

my %cidr_to_netmask = (
	32 => "255.255.255.255",
	31 => "255.255.255.254",
	30 => "255.255.255.252",
	29 => "255.255.255.248",
	28 => "255.255.255.240",
	27 => "255.255.255.224",
	26 => "255.255.255.192",
	25 => "255.255.255.128",
	24 => "255.255.255.0",
	23 => "255.255.254.0"
);

while (<>) {
	chomp;
	if (/^(.*?)(\d+\.\d+\.\d+\.\d+)\/(\d+)(.*)$/) {
		printf "%s%s %s%s\n", $1, $2, $cidr_to_netmask{$3}, $4;
	} else {
		print "$_\n";
	}
}
